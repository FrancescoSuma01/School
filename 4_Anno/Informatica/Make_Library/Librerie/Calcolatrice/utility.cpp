#include "utility.h"

float somma(int num1, int num2){
    float addizione = 0.0;

    addizione = num1 + num2;

    return addizione;
}

float differenza(int num1, int num2){
    float sottrazione = 0.0;

    sottrazione = num1 - num2;

    return sottrazione;
}

float prodotto(int num1, int num2){
    float moltiplicazione = 0.0;

    moltiplicazione = num1 * num2;

    return moltiplicazione;
}

float quoziente(int num1, int num2){
    float divisione = 0.0;

    divisione = num1 / num2;

    return divisione;
}
